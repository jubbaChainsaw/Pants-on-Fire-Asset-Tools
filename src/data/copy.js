export function createCopyPicker(bank) {
  const history = new Map();
  return function getLine(key) {
    const lines = bank[key] || [];
    if (!lines.length) return '';
    const prev = history.get(key) || [];
    const filtered = lines.filter((line) => !prev.includes(line));
    const chosen = (filtered.length ? filtered : lines)[Math.floor(Math.random() * (filtered.length ? filtered.length : lines.length))];
    history.set(key, [...prev, chosen].slice(-3));
    return chosen;
  };
}
